# 1 3  7      41

# (dp[1][0]) = 3
# (dp[1][1]) = 2 + 1 * 2
# (dp[1][2]) = 7

# (dp[2][0]) = dp[1][2] -> 7
# (dp[2][1]) = dp[1][1] + dp[1][0] * 2 -> 10 
# (dp[2][2]) = 17

# (dp[3][0]) = dp[2][2] -> 17
# (dp[3][1]) = dp[2][1] + dp[2][0] * 2 -> 24
# (dp[3][2]) = 41


N = int(input())
dp = [[0,0,0] for _ in range (N + 1)]
dp[0][0] = 1
dp[0][1] = 0
dp[0][1] = 1

for i in range(1, N + 1) :
  dp[i][0] = dp[i - 1][2] % 100000
  dp[i][1] = (dp[i - 1][1] + 2 * dp[i - 1][0]) % 100000
  dp[i][2] = (dp[i][0] + dp[i][1]) % 100000

print(dp[N][2])